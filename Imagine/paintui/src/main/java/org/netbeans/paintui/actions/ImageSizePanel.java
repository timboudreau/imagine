    private Dimension preferredImageSize = new Dimension (640, 480);
    public Dimension getDimension() {
	Dimension result = imgSize == null ? preferredImageSize : imgSize;
	boolean bad = false;
	try {
	    result.width = Integer.parseInt(jTextField1.getText().trim());
	} catch (NumberFormatException nfe) {
	    bad = true;
	}
	try {
	    result.height = Integer.parseInt(jTextField2.getText().trim());
	} catch (NumberFormatException nfe) {
	    bad = true;
	}
	if (bad) {
	    errorText.setText (NbBundle.getMessage(ImageSizePanel.class,
		    "LBL_BadInput")); //NOI18N
	} else {
	    preferredImageSize = new Dimension (result);
	}
	return result;
    }
    
